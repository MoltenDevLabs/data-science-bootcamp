# Escribir un programa que almacene en una llsita los siguientes precios: 50, 75, 46, 22, 80, 65, 8 y muestre por pantalla el menor y el mayor de los precios

p = [50, 75, 46, 22, 80, 65, 8]                   # Se inicializa una lista p con diversos numeros

men = min(p)                                      # Se guarda el menor en la variable men
may = max(p)                                      # Se guarda el mayor en la variable may

print(f"El menor es {men} y el mayor es {may}")   # Se imprime cuales son el menor y el mayor